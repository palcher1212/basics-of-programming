#include <stdio.h>
#include <stdlib.h>
#include <time.h>

void find_row_with_max_sum(int rows, int cols) {
    // создаем двумерный массив (матрицу)
    int matrix[rows][cols];
    
    // генератор случайных чисел
    srand(time(NULL));

    // заполняем матрицу случайными числами от 1 до 10
    for (int i = 0; i < rows; i++) {
        for (int j = 0; j < cols; j++) {
            matrix[i][j] = rand() % 10 + 1; // случайные числа от 1 до 10
        }
    }

    // выводим сгенерированную матрицу
    printf("Сгенерированная матрица:\n");
    for (int i = 0; i < rows; i++) {
        for (int j = 0; j < cols; j++) {
            printf("%d ", matrix[i][j]);
        }
        printf("\n"); // переход на новую строку после каждой строки матрицы
    }

    // переменные для хранения номера строки с максимальной суммой и самой суммы
    int max_sum_row = -1; // номер строки с максимальной суммой
    int max_sum = 0; // максимальная сумма

    // проходим по всем строкам матрицы
    for (int i = 0; i < rows; i++) {
        int current_sum = 0; // текущая сумма элементов строки
        
        // вычисляем сумму элементов текущей строки
        for (int j = 0; j < cols; j++) {
            current_sum += matrix[i][j];
        }

        // проверяем, является ли текущая сумма больше максимальной
        if (current_sum > max_sum) {
            max_sum = current_sum; // обновляем максимальную сумму
            max_sum_row = i; // обновляем номер строки с максимальной суммой
        }
    }

    // выводим номер строки с наибольшей суммой и значение этой суммы
    printf("Номер строки с наибольшей суммой: %d\n", max_sum_row + 1); // +1 для удобства пользователя
    printf("Наибольшая сумма: %d\n", max_sum);
}

int main() {
    int M, N;

    // ввод размеров матрицы от пользователя
    printf("Введите количество строк (M): ");
    scanf("%d", &M);
    
    printf("Введите количество столбцов (N): ");
    scanf("%d", &N);

    // вызываем функцию для поиска строки с максимальной суммой
    find_row_with_max_sum(M, N);

    return 0;
}